    private void regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regActionPerformed
          if(adl==null)
        {
            adl = new AdoptionList();
        }
        Adoption app = adl.addAdoption();
          int row = tblpet.getSelectedRow();
       if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dur;
        double inc;
        try{
         dur = Integer.parseInt(txtduration.getText());
        
         inc = Double.parseDouble(txtincome.getText());
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please check if you have entered all the values", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String reason = String.valueOf(txtreason.getText());
           if (reason.equals("") ) {
            JOptionPane.showMessageDialog(this, "Please check if you have entered all the values", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Pet pet = (Pet) tblpet.getValueAt(row, 0);
        Pet n = pet;
        app.setPet(n);
        app.setIncome(inc);
        app.setDuration(dur);
        app.setUser(user);
        app.setStatus("open");
        os.setPetDirectory(petDirectory);
        JOptionPane.showMessageDialog(null, "Register to adopt that pet !", "Info", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_regActionPerformed

