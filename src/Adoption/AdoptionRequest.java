    private void populateTable() {
       
     DefaultTableModel model = (DefaultTableModel) tblpet.getModel();
        model.setRowCount(0);
        System.out.println("populate on adopt table");
        //  System.out.println(p.getHouse().getCommunity().getCity().getCityName());
        for (Pet p : petDirectory.getPetlist()) {
           if(p.getPetType().equalsIgnoreCase("Adoption"))
           {
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getAge();
            row[2] = p.getBreed();
          

            model.addRow(row);
    }
        }
    
    }
